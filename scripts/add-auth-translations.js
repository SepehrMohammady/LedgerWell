// Function to add translations to a locale file
function addAuthTranslations(localeCode) {
  const filePath = path.join(__dirname, '..', 'src', 'locales', `${localeCode}.json`);
  
  try {
    // Read existing translations
    const fileContent = fs.readFileSync(filePath, 'utf8');
    const existingTranslations = JSON.parse(fileContent);
    
    // Merge with new auth translations
    const updatedTranslations = {
      ...existingTranslations,
      ...authTranslations[localeCode]
    };
    
    // Write back to file
    fs.writeFileSync(
      filePath,
      JSON.stringify(updatedTranslations, null, 2) + '\n',
      'utf8'
    );
    
    console.log(`✓ Updated ${localeCode}.json`);
  } catch (error) {
    console.error(`✗ Error updating ${localeCode}.json:`, error.message);
  }
}

// Main execution
console.log('Adding authentication translations to all locale files...\n');

const locales = Object.keys(authTranslations);
locales.forEach(locale => {
  addAuthTranslations(locale);
});

console.log('\nAuthentication translation update complete!');
